"""
Calculator functions
"""


def add(x, y):
    """Add two numbers"""
    return x + y


def subtract(x, y):
    """Subtract x from y and return value"""
    return y - x


def multiply(x, y):
    """Multiply two numbers"""
    return x * y


def divide(x, y):
    """Divide x by y and return value"""
    return x / y
